	// Get record filter
	public function getRecordFilter($row = NULL)
	{
		$keyFilter = $this->sqlKeyFilter();
		if (is_array($row))
			$val = array_key_exists('idcliente_log', $row) ? $row['idcliente_log'] : NULL;
		else
			$val = $this->idcliente_log->OldValue !== NULL ? $this->idcliente_log->OldValue : $this->idcliente_log->CurrentValue;
		if (!is_numeric($val))
			return "0=1"; // Invalid key
		if ($val == NULL)
			return "0=1"; // Invalid key
		else
			$keyFilter = str_replace("@idcliente_log@", AdjustSql($val, $this->Dbid), $keyFilter); // Replace key value
		return $keyFilter;
	}

